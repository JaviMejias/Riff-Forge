function getFingers(fretsStr) {
  if (openFingerMap[fretsStr]) return openFingerMap[fretsStr];
  
  const f = JSON.parse(fretsStr);
  
  // A-shape barre (root on 5th string)
  if (f[0] === -1 && f[1] > 0 && f[1] === Math.min(...f.filter(x => x > 0))) {
    const r = f[1];
    if (f[2] === r+2 && f[3] === r+2 && f[4] === r+2 && f[5] === r) return `[0, 1, 2, 3, 4, 1]`;
    if (f[2] === r+2 && f[3] === r+2 && f[4] === r+1 && f[5] === r) return `[0, 1, 3, 4, 2, 1]`;
    if (f[2] === r+2 && f[3] === r && f[4] === r+2 && f[5] === r) return `[0, 1, 3, 1, 4, 1]`;
    if (f[2] === r+2 && f[3] === r && f[4] === r+1 && f[5] === r) return `[0, 1, 3, 1, 2, 1]`;
    if (f[2] === r+2 && f[3] === r+1 && f[4] === r+2 && f[5] === r) return `[0, 1, 3, 2, 4, 1]`;
    if (f[2] === r+2 && f[3] === r+2 && f[4] === r+3 && f[5] === r) return `[0, 1, 2, 3, 4, 1]`;
    if (f[2] === r+2 && f[3] === r+2 && f[4] === r && f[5] === r) return `[0, 1, 3, 4, 1, 1]`;
    if (f[2] === r+1 && f[3] === r-1 && f[4] === r+1 && f[5] === -1) return `[0, 2, 3, 1, 4, 0]`;
  }

  // E-shape barre (root on 6th string)
  if (f[0] > 0 && f[0] === Math.min(...f.filter(x => x > 0))) {
    const r = f[0];
    if (f[1] === r+2 && f[2] === r+2 && f[3] === r+1 && f[4] === r && f[5] === r) return `[1, 3, 4, 2, 1, 1]`;
    if (f[1] === r+2 && f[2] === r+2 && f[3] === r && f[4] === r && f[5] === r) return `[1, 3, 4, 1, 1, 1]`;
    if (f[1] === r+2 && f[2] === r && f[3] === r+1 && f[4] === r && f[5] === r) return `[1, 3, 1, 2, 1, 1]`;
    if (f[1] === r+2 && f[2] === r && f[3] === r && f[4] === r && f[5] === r) return `[1, 3, 1, 1, 1, 1]`;
    if (f[1] === r+2 && f[2] === r+2 && f[3] === r+2 && f[4] === r && f[5] === r) return `[1, 3, 4, 4, 1, 1]`;
    if (f[1] === r+2 && f[2] === r+2 && f[3] === 0 && f[4] === r && f[5] === r) return `[1, 3, 4, 0, 1, 1]`;
    if (f[1] === r+1 && f[2] === r+2 && f[3] === r && f[4] === r+2 && f[5] === r) return `[1, 2, 3, 1, 4, 1]`;
  }

  return null;
}

const lines = code.split('\n');
const outLines = [];
let missing = 0;

for (const line of lines) {
  const match = line.match(/(frets: (\[.*?\]))/);
  if (match && !line.includes('fingers:')) {
    const fretsStr = match[2];
    const fingers = getFingers(fretsStr);
    if (fingers) {
      outLines.push(line.replace(match[1], `${match[1]}, fingers: ${fingers}`));
    } else {
      console.log('Missing mapping for:', fretsStr, 'Line:', line.trim());
      missing++;
      outLines.push(line);
    }
  } else {
    outLines.push(line);
  }
}

fs.writeFileSync(filePath, outLines.join('\n'));
console.log('Processed. Missing:', missing);
