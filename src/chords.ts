const normalizeName = (name: string): string => {
  const match = name.match(/^([A-Ga-g][#b]?)(.*)$/);
  if (!match) return name.toLowerCase();
  const root = match[1].toLowerCase();
  const suffix = match[2].toLowerCase();
  const rootAliases: Record<string, string> = {
    "db": "c#", "d#": "eb", "gb": "f#", "ab": "g#", "a#": "bb"
  };
  return (rootAliases[root] || root) + suffix;
};

export const getChord = (name: string): ChordDef | undefined => {
  if (!name) return undefined;
  let searchName = name.trim();
  if (searchName.includes('/')) {
    const parts = searchName.split('/');
    if (isNaN(Number(parts[1]))) {
      searchName = parts[0].trim();
    }
  }
  const targetName = normalizeName(searchName);
  let found = CHORD_DICTIONARY.find(c => c.name.toLowerCase() === targetName);
  if (!found) {
    if (targetName.endsWith('dim')) {
      found = CHORD_DICTIONARY.find(c => c.name.toLowerCase() === targetName + '7');
    } else if (targetName.endsWith('dim7')) {
      found = CHORD_DICTIONARY.find(c => c.name.toLowerCase() === targetName.replace('dim7', 'dim'));
    }
  }
  if (found) {
    return {
      ...found,
      name: name
    };
  }
  return undefined;
};